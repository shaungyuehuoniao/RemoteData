package DataAn.Analysis.dto;


public class SingleParamDto {
	private int id;
	
	private String name;
	
	private int max;
	
	private int min;
	
	private String y2Name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public String getY2Name() {
		return y2Name;
	}

	public void setY2Name(String y2Name) {
		this.y2Name = y2Name;
	}
	
	
	
}		
